package com.example.pizzeria.models;

/**
 * Enum representing the various toppings available for pizzas.
 *
 * This enum defines a list of common toppings that can be added
 * to different types of pizzas. It is used to specify the ingredients
 * included in specific pizza types or when building custom pizzas.
 *
 * @author Lukas Chang
 */
public enum Topping {
    SAUSAGE,
    PEPPERONI,
    GREEN_PEPPER,
    ONION,
    MUSHROOM,
    BBQ_CHICKEN,
    PROVOLONE,
    CHEDDAR,
    BEEF,
    HAM,
    OLIVE,
    SPINACH,
    PINEAPPLE
}
